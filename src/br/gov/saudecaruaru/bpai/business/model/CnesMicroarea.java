public class CnesMicroarea {
    private String controle;
    private String codigoUnidade;
    private String nomeMicroarea;
    private String codigoMicroarea;
    private String cbo;
    private String codigoDesconhecido;

    /**
     * @return the controle
     */
    public String getControle() {
        return controle;
    }

    /**
     * @param controle the controle to set
     */
    public void setControle(String controle) {
        this.controle = controle;
    }

    /**
     * @return the codigoUnidade
     */
    public String getCodigoUnidade() {
        return codigoUnidade;
    }

    /**
     * @param codigoUnidade the codigoUnidade to set
     */
    public void setCodigoUnidade(String codigoUnidade) {
        this.codigoUnidade = codigoUnidade;
    }

    /**
     * @return the nomeMicroarea
     */
    public String getNomeMicroarea() {
        return nomeMicroarea;
    }

    /**
     * @param nomeMicroarea the nomeMicroarea to set
     */
    public void setNomeMicroarea(String nomeMicroarea) {
        this.nomeMicroarea = nomeMicroarea;
    }

    /**
     * @return the codigoMicroarea
     */
    public String getCodigoMicroarea() {
        return codigoMicroarea;
    }

    /**
     * @param codigoMicroarea the codigoMicroarea to set
     */
    public void setCodigoMicroarea(String codigoMicroarea) {
        this.codigoMicroarea = codigoMicroarea;
    }

    /**
     * @return the cbo
     */
    public String getCbo() {
        return cbo;
    }

    /**
     * @param cbo the cbo to set
     */
    public void setCbo(String cbo) {
        this.cbo = cbo;
    }

    /**
     * @return the codigoDesconhecido
     */
    public String getCodigoDesconhecido() {
        return codigoDesconhecido;
    }

    /**
     * @param codigoDesconhecido the codigoDesconhecido to set
     */
    public void setCodigoDesconhecido(String codigoDesconhecido) {
        this.codigoDesconhecido = codigoDesconhecido;
    }
   
    
    
}
